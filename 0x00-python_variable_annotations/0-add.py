#!/usr/bin/env python3
"""A function that add two float and return float"""


def add(a: float, b: float) -> float:
    """
    Add two floats and return their sum.

    Args:
        a (float): The first float to add.
        b (float): The second float to add.

    Returns:
        float: The sum of a and b as a float.
    """
    return a + b
